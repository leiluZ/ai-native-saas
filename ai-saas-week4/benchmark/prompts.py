def get_prompts_by_length(length: str = "all", count: int = 100) -> List[str]:
    if length == "short":
        prompts = SHORT_PROMPTS
    elif length == "medium":
        prompts = MEDIUM_PROMPTS
    elif length == "long":
        prompts = LONG_PROMPTS
    else:
        prompts = SHORT_PROMPTS + MEDIUM_PROMPTS + LONG_PROMPTS

    selected = []
    while len(selected) < count:
        selected.extend(prompts[:min(len(prompts), count - len(selected))])

    return selected[:count]


def estimate_token_count(text: str, chars_per_token: float = 4.0) -> int:
    return int(len(text) / chars_per_token)
